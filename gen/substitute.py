#!/usr/bin/env python3

import re
import argparse
from pathlib import Path


def substitute(s: str, vars: list[tuple[str, str]]) -> str:
    out = s
    for var, value in vars:
        out = re.sub(f"@{var}@", value, out)
    return out


def key_value(s: str) -> tuple[str, str]:
    sep = s.index(":")
    return s[:sep], s[sep + 1 :]


def main() -> None:
    parser = argparse.ArgumentParser(
        prog="substitute",
        description="Substitute CMake-style variables (i.e. @VAR@) for given values",
    )
    parser.add_argument(
        "input", type=Path, help="input file path to run substitutions on"
    )
    parser.add_argument(
        "-o", "--output", type=Path, required=True, help="the output file path"
    )
    parser.add_argument(
        "-v",
        "--values",
        type=key_value,
        nargs="*",
        help="variables and their values of the form VAR:VALUE",
    )

    args = parser.parse_args()
    contents = args.input.read_text(encoding="UTF-8")
    substituted = substitute(contents, args.values)
    args.output.write_text(
        f"""/*
This file was generated by gen/substitute.py.

Original file: {args.input}
*/

"""
        + substituted
    )


if __name__ == "__main__":
    main()
